# Base arithmetic operations
print("Base arithmetic operations between integers:")
print(2 + 3)
print(2 - 3)
print(2 * 3)
print(2 ** 3)
print(7 / 4)
print(7 // 4)
print(7 % 4)
print("Base arithmetic operations between float:")
print(2.3 + 7.7)
print(2.3 - 7.7)
print(2.3 * 7.7)
print(7.7 ** 2.3)
print(7.7 / 2.3)
print(7.7 // 2.3)
print(7.7 % 2.3)

# Python3 have dynamic data type
print("Base arithmetic expressions:")
x = 5
print(x)
x = "Hello!"
print(x)

# How to know type of variables cause of dynamic type issues
print("How to know type of variables cause of dynamic type issues:")
x = "Hello!"
type_of_variable = type(x)
print(type_of_variable)
x = 7
type_of_variable = type(x)
print(type_of_variable)
x = 5.6
type_of_variable = type(x)
print(type_of_variable)

