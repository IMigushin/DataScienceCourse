# Base arithmetic operations
print("Base arithmetic operations between integers:")
print(2 + 3)
print(2 - 3)
print(2 * 3)
print(2 ** 3)
print(7 / 4)
print(7 // 4)
print(7 % 4)

print("Base arithmetic operations between float:")
print(2.3 + 7.7)
print(2.3 - 7.7)
print(2.3 * 7.7)
print(7.7 ** 2.3)
print(7.7 / 2.3)
print(7.7 // 2.3)
print(7.7 % 2.3)



